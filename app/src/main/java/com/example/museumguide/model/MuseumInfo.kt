package com.example.museumguide.model

/**
 * Data class for a museum with its basic info and notable exhibits.
 * These are locally embedded — no network fetch needed.
 */
data class MuseumInfo(
    val id: Int,
    /** Museum name in Chinese (e.g. "故宫博物院") */
    val name: String,
    /** City location */
    val city: String,
    /** Brief description (short, for display) */
    val brief: String,
    /** Full description */
    val description: String,
    /** Cultural/historical significance */
    val significance: String,
    /** Approximate latitude */
    val latitude: Double,
    /** Approximate longitude */
    val longitude: Double,
    /** List of notable exhibits in this museum */
    val exhibits: List<MuseumExhibit>
)

/**
 * A notable exhibit/artifact within a museum.
 */
data class MuseumExhibit(
    /** Exhibit name (e.g. "清明上河图") */
    val name: String,
    /** Dynasty/era */
    val era: String,
    /** Brief description */
    val description: String
)

